let num = [5, 8, 2, 9, 3] //vetor com vários valores

console.log(`Nosso vetor é o ${num}`) //mostra todos os valores
console.log(`Nosso vetor é o ${num[3]}`) //mostra apenas o valor do índice 3

num [5] = 6 //acrescenta o novo valor 10 ao índice [5]

console.log(num) 

num [0] = 1 //troca o valor 5 pelo novo valor 1 no índice [0]

console.log(num) 

num [10] = 7 // acrescenta o novo valor 7 ao índice [10] e deixam vazios
// os índices [6], [7], [8] e [9].

console.log (num)

num.push(4) //.push cria um elemento na última posição do array e 
//acrescenta o valor entre parênteses dentro desse elemento

console.log (num)

console.log(num.length) //.length mostra o comprimento do vetor, ou seja,
// a quantidade de elementos que o compõe

console.log(num.sort()) //.sort põe todos os elementos em ordem crescente


